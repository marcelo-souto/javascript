// FUNCTION TRADICIONAL
function somar (x,y) {
    return x + y
};

// FUNCTION POR MEIO DE UMA VARIAVEL
const somar = function (x,y) {
    return x + y
};

// FUNCTION ARROW
const somar = (x,y) => {
    return x + y
};

// FUNCTION ARROW MAIS SIMPLIFICADA
const somar = (x,y) => x + y;

const letrasnoNome = (nome) => nome.length;

// FUNCTION COM APENAS UM PARAMETRO
const letrasnoNome = nome => nome.length;
